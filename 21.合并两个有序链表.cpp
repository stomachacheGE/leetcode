/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* mergeTwoLists(ListNode* l1, ListNode* l2) {
        // solution one (v2)
        ListNode* dummy = new ListNode(-1), *node = dummy;
        while (l1 && l2) {
            if (l1->val < l2->val) {
                node->next = l1;
                l1 = l1->next;
            } else {
                node->next = l2;
                l2 = l2->next;
            }
            node = node->next;
            node->next = NULL;
        }
        if (l1) node->next = l1;
        if (l2) node->next = l2;
        return dummy->next;
    }
};
// @lc code=end

